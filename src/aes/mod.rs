fn galois_mult(val: u8, coeff: u8) -> u8 {
    let val1 = val as usize;

    match coeff {
        1 => val,
        2 => GF_2[val1],
        3 => GF_3[val1],
        9 => GF_9[val1],
        11 => GF_11[val1],
        13 => GF_13[val1],
        14 => GF_14[val1],
        _ => panic!("Did not expect"),
    }
}

fn col_mul(col: &[u8], mat: &[u8; 4]) -> u8 {
    let mut sum = 0u8;

    for idx in 0..4 {
        sum ^= galois_mult(col[idx], mat[idx]);
    }
    sum
}

type Block = [u8; 16];

fn print_block(block: &Block) {
    println!("{:?}", block);
}

fn _sub_bytes(block: &mut Block, sub: &[u8; 256]) {
    for val in block.iter_mut() {
        *val = sub[*val as usize];
    }
}
fn sub_bytes(block: &mut Block) {
    _sub_bytes(block, &S_BOX);
}
fn i_sub_bytes(block: &mut Block) {
    _sub_bytes(block, &I_S_BOX);
}

fn shift_rows(block: &mut Block) {
    let mut slice = [0u8; 4];
    let mut row = 0;
    for pos in (0..block.len()).step_by(4) {
        slice.copy_from_slice(&block[pos..pos + 4]);

        for col in 0..4 {
            let mut idx = col + row;
            if idx >= 4 {
                idx -= 4;
            }
            block[pos + col] = slice[idx];
        }

        row += 1;
    }
}

fn i_shift_rows(block: &mut Block) {
    let mut slice = [0u8; 4];
    let mut row = 0;
    for pos in (0..block.len()).step_by(4) {
        slice.copy_from_slice(&block[pos..pos + 4]);

        for col in 0u8..4 {
            let mut idx: i8 = col as i8 - row as i8;
            if idx < 0 {
                idx += 4;
            }
            block[pos + col as usize] = slice[idx as usize];
        }

        row += 1;
    }
}

fn _mix_columns(block: &mut Block, matrix: &[[u8; 4]; 4]) {
    for col_idx in 0..4 {
        let slice = [
            block[0 + col_idx],
            block[4 + col_idx],
            block[8 + col_idx],
            block[12 + col_idx],
        ];
        for row_idx in 0..4 {
            let pos = row_idx * 4 + col_idx;
            block[pos] = col_mul(&slice, &matrix[row_idx]);
        }
    }
}
pub fn mix_columns(block: &mut Block) {
    _mix_columns(block, &COL_MATRIX);
}
fn i_mix_columns(block: &mut Block) {
    _mix_columns(block, &I_COL_MATRIX);
}

fn add_round_key(data: &mut Block, key: &Block) {
    for pos in 0..data.len() {
        data[pos] = data[pos] ^ key[pos];
    }
}

fn pad_string(data: &[u8]) -> std::vec::Vec<u8> {
    let rem = 16 - (data.len() % 16);

    let new_len = data.len() + rem;

    let mut slice = vec![0; new_len];
    slice[..data.len()].copy_from_slice(&data);
    return slice;
}

pub fn encrypt(data: &[u8], key: &[u8], rounds: u8) -> std::vec::Vec<u8> {
    let mut b_key = [0u8; 16];

    let mut res: std::vec::Vec<u8> = Vec::new();

    let new_data = pad_string(data);
    let new_key = pad_string(key);
    b_key.copy_from_slice(&new_key);

    for pos in (0..new_data.len()).step_by(16) {
        let mut slice: Block = [0u8; 16];
        slice.copy_from_slice(&new_data[pos..pos + 16]);

        for _round in 0..rounds {
            sub_bytes(&mut slice);
            shift_rows(&mut slice);
            mix_columns(&mut slice);
            add_round_key(&mut slice, &b_key);
        }

        res.append(&mut slice.to_vec());
        break;
    }

    res
}

pub fn decrypt(data: &[u8], key: &[u8], rounds: u8) -> std::vec::Vec<u8> {
    let mut b_key = [0u8; 16];

    let mut res: std::vec::Vec<u8> = Vec::new();

    let new_data = pad_string(data);
    let new_key = pad_string(key);
    b_key.copy_from_slice(&new_key);

    for pos in (0..new_data.len()).step_by(16) {
        let mut slice: Block = [0u8; 16];
        slice.copy_from_slice(&new_data[pos..pos + 16]);

        for _round in 0..rounds {
            add_round_key(&mut slice, &b_key);
            i_mix_columns(&mut slice);
            i_shift_rows(&mut slice);
            i_sub_bytes(&mut slice);
        }

        res.append(&mut slice.to_vec());
        break;
    }

    res
}
